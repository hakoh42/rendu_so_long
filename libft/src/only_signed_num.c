#include "../inc/libft.h"

int	only_signed_num(char *str)
{
	int	i;

	if (!str)
		return (0);
	if (!((str[0] >= '0' && str[0] <= '9') || str[0] == '-' || str[0] == '+'))
		return (0);
	i = 1;
	while (str[i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}

#include "libftprintf.h"

/*
** int len
 */
int	ft_nb_len(int n)
{
	long				res;
	int					len;

	len = 1;
	res = n;
	if (n < 0)
	{
		res *= -1;
		len++;
	}
	while (res > 9)
	{
		res /= 10;
		len++;
	}
	return (len);
}
